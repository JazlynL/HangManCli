import java.util.Scanner;

public class GameLife {

    public int lives = 4;


    public void life() {
        System.out.println("Welcome to HangMan!");

        while (lives > 0) {
            System.out.println("Lives: ");

            for (int i = 0; i < lives; i++) {
                System.out.println("0");

            }
            System.out.println();



            break;






        }
    }


}
